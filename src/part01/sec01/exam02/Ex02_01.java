package part01.sec01.exam02;

public class Ex02_01 {

	public static void main(String[] args) {
			
		int a;
		int b;
		int result;
		
		a=100;
		b=50;
		
		result =a+b;
		System.out.println(a+b); //덧셈 연산
		System.out.println(a+"+"+b+"="+result); // 변수(a)+"문자열" : +연결
		result=a-b;
		System.out.println(a+"-"+b+"="+result);
		result=a*b;
		System.out.println(a+"*"+b+"="+result);
		result=a/b;
		System.out.println(a+"/"+b+"="+result);
		
	}

}
